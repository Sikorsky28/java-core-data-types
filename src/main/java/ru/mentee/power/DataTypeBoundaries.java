package ru.mentee.power;

public class DataTypeBoundaries {
    public static void main(String[] args) {

        System.out.println("byte: от " + Byte.MIN_VALUE + " до " + Byte.MAX_VALUE);
        System.out.println("short: от " + Short.MIN_VALUE + " до " + Byte.MAX_VALUE);
        System.out.println("int: от " + Integer.MIN_VALUE + " до " + Byte.MAX_VALUE);
        System.out.println("double: от " + Double.MIN_VALUE + " до " + Byte.MAX_VALUE);
        System.out.println("float: от " + Float.MIN_VALUE + " до " + Byte.MAX_VALUE);
        System.out.println("long: от " + Long.MIN_VALUE + " до " + Byte.MAX_VALUE);
        System.out.println("char: от " + Character.MIN_VALUE + " до " + Byte.MAX_VALUE);

        System.out.println("Максимальное значение byte: " + Byte.MAX_VALUE);
        System.out.println("После прибавленя 1: " + Byte.MIN_VALUE);

    }
}